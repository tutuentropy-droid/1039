export const getNodesByPeriod = (startYear: number, endYear: number): MapNode[] => {
  return mapNodes.filter(node => node.year >= startYear && node.year <= endYear);
};

export const getNodesByType = (type: string): MapNode[] => {
  return mapNodes.filter(node => node.type === type);
};

export const getNodeById = (id: string): MapNode | undefined => {
  return mapNodes.find(node => node.id === id);
};

export const parseYearString = (yearStr: string): number => {
  if (!yearStr) return 0;
  const clean = yearStr.replace(/年|约|前后|世纪/g, '').trim();
  const match = clean.match(/前?\s*(\d+)/);
  if (match) {
    const year = parseInt(match[1]);
    return clean.includes('前') ? -year : year;
  }
  return 0;
};
