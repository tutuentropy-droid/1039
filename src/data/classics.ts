export const getClassicById = (id: string): ClassicBook | undefined => {
  return classics.find(book => book.id === id);
};

export const getClassicsByCategory = (category: ClassicBook['category']): ClassicBook[] => {
  return classics.filter(book => book.category === category);
};

export const getChapterById = (bookId: string, chapterId: string): ClassicChapter | undefined => {
  const book = getClassicById(bookId);
  return book?.chapters.find(chapter => chapter.id === chapterId);
};
